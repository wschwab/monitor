const FIXTURES: Array<{ match: (prompt: string) => boolean; fixture: DemoPromptFixture }> = [
  {
    match: (prompt) => {
      const normalized = normalizePrompt(prompt);
      return normalized.includes('kyouma') || normalized.includes('classified cern dossier');
    },
    fixture: KYOUMA_FIXTURE,
  },
];

export function getDemoPromptFixture(prompt: string): DemoPromptFixture | undefined {
  const normalized = normalizePrompt(prompt);
  return FIXTURES.find(({ match }) => match(normalized))?.fixture;
}

export function buildGenericCoverImageFixture(prompt: string): DemoCoverImageFixture {
  const topic = prompt.trim() || 'Research brief';
  const slug = slugify(topic) || 'research-brief';

  return {
    imageUrl: `https://demo.monitor/assets/generic-${slug}.png`,
    title: 'Research Brief Cover Image',
    alt: `Illustrated editorial cover for: ${topic}`,
    prompt: `Editorial research cover with charts, annotations, and a cinematic evidence-board layout for: ${topic}`,
  };
}
